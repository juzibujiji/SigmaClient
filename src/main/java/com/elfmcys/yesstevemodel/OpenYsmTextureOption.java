package com.elfmcys.yesstevemodel;

public final class OpenYsmTextureOption {
    private final String id;
    private final String path;

    public OpenYsmTextureOption(String id, String path) {
        this.id = id;
        this.path = path;
    }

    public String getId() {
        return this.id;
    }

    public String getPath() {
        return this.path;
    }
}
